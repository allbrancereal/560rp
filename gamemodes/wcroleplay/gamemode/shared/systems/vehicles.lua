function _pMeta:StartVehicleShop( )
		
	if self:getFlag("inVehicleShop", false) != false then return end
	if self:Team() != TEAM_CIVILLIAN then return self:Notify("You can not buy a vehicle while on duty.") end
	//makePropDisplayFromTable()
	self:setFlag("inVehicleShop", true )
	
	
	net.Start("sendClientVehicleShop")
		net.WriteBool( false )
	net.Send( self )
	
end

function _pMeta:EndVehicleShop( )

	if self:getFlag("inVehicleShop", false) != true then return end
	
	self:setFlag("inVehicleShop", false )
	
	net.Start("sendClientVehicleShop")
		net.WriteBool( true )
	net.Send( self )
	
