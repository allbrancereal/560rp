	function fsrp.Vehicle.MakeVehicleSongThink()
	
		LocalPlayer():setFlag("isThinkingVehicleCameraToSong", true)		
		_skippedIntro = false;
		hook.Add("Think", "VehicleSongThink", function()
				
			local _VehicleSong = LocalPlayer().VehicleDisplaySong;
			
			if _VehicleSong then
				local _cam = math.Round(_VehicleSong:GetTime(), 1);
				
				if fsrp.Vehicle._VehicleCameraSteps[_cam] && fsrp.Vehicle._VehicleCameraSteps[_cam].val !=  LocalPlayer():getFlag("vehicleCamera", 0 ) then
					
					LocalPlayer():setFlag( "vehicleCamera",  fsrp.Vehicle._VehicleCameraSteps[_cam].val )
									
				end
				
				if _VehicleSong:GetTime() > 25 && !_skippedIntro then
				
					hook.Run( "ClientFinishedVehicleShopIntro" )
					_skippedIntro = true;
					fsrp.Vehicle.RemoveVehicleSongThink()
					
				end
				
			else
			
				hook.Remove( "Think", "VehicleSongThink" )
				
			end
						
		end )
	
	end
	function fsrp.Vehicle.SkipVehicleIntro()
	
		if hook.GetTable()["Think"]["VehicleSongThink"] then 
		
		
			local _VehicleSong = LocalPlayer().VehicleDisplaySong;
			
			if _VehicleSong then
							
				_VehicleSong:SetTime( 25 )
				LocalPlayer():setFlag("vehicleCamera", 0 )
				
				hook.Run( "ClientFinishedVehicleShopIntro" )
				
				fsrp.Vehicle.RemoveVehicleSongThink()	
				
			
			end
				
		end
	
	end
	
